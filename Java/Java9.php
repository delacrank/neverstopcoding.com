    <h1>Method Overloading - Java Programming</h1>
    
    <h2>Method Overloading</h2>
    
        <p>Let's talk about method overloading and how we can use this to intialize varying fields for our constructors.
    
    <span class = "nl">Let's say we have a method like 'add(int x, int y)' that takes two parameter integers. However, let's say that we want to pass three integers, or two doubles, or concat two strings, while still using the same method name.</span>
    
    <span class = "nl">The way to do this is to redeclare the method and changing the parameters, the new method will be called based on the types of arguments that get passed into it. Let's see what this would look like.</span></p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    class methodOverloading {
    
        public static int add(int x, int y)
        {
            return x + y;
        }
        
        public static int add(int x, int y, int z)
        {
            return x + y + z;
        }
        
        public static double add(double x, double y)
        {
            return x + y;
        }
        
        public static String add(String x, String y)
        {
            return x + " " + y;
        }
    
        public static void main(String[] args)
        {
            System.out.println("Add 5 + 3: " + add(5, 3));
            
            System.out.println("Add 5.5 + 3.5: " + add(5.5,  3.5));
            
            System.out.println("Add 5 + 3 + 1" + add(5, 3, 1));
            
            System.out.println("Add String 'cool', String 'name'" + add("cool", "name"));
        }
    }</pre>
    
    <p>Now, let's see how this works with constructors. Let me make another employee class and create an object initializing varying amounts of fields.</p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    class constructorOverloading {
    
        public static void main(String[] args)
        {
            Employee A = new Employee();
            A.Display();
            
            Employee B = new Employee("Carlos");
            B.Display();
            
            Employee C = new Employee("Sue", 1500);
            C.Display();
            
            Employee D = new Employee("June", 2500, 40);
            D.Display();
        }
    }
    
    class Employee {
    
        private String name;
        private int salary;
        private int hours;
    
        public Employee()
        {
        
        }
        
        public Employee(String name)
        {
            this.name = name;
        }
        
        public Employee(String name, int salary)
        {
            this.name = name;
            this.salary = salary;
        }
        
        public Employee(String name, int salary, int hours)
        {
            this.name = name;
            this.salary = salary;
            this.hours = hours;
        }
    
        public void Display()
        {
            System.out.printf("Name: %s, Salary %d, Hours %d\n", name, salary, hours );
        }
    
    }
