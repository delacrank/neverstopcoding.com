        <!-- Constructor Lists -->    
        <h3>Constructor List</h3>
        
            <p>A constructor list is a way we are able to initialize variables for our classes.  Similar to the way we have initialized our variables inside the class using constructor when declaring member functions, we can also declare the constructor outside of our header or class file.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class = "code">
    class Person
    {
    public:
        <span class="comments">// Declare your constructor first</span>
        Person();
        
    private:
        int var;
        int var2;
    };
    
    <span class="comments">// Constructor Initializer List</span>
    Person::Person()
    {
        var = 5;
        var2 = 10;
    }</pre>
        
        <p>Be sure that when using a constructor initializer that you declare the constructor function in your class.  Let's look at an example if you wanted to initialize your own private variables.</p>
        
        <em>Code:</em>
    <!-- code -->
    <pre class = "code">
    class Person
    {
    public:
        <span class="comments">// Declare your constructor parameters</span>
        Person(int, int);
        
    private:
        int var;
        int var2;
    };
    
    Person::Person(int x, int y)
    {
    <span class="comments">// Initialize your private member variables to your parameters</span>
        var = x;
        var2 = y;
    }
    
    int main()
    {
        Person p(5, 10);
    }</pre>
    <hr />
        
        <!-- delegating Constructors -->
        <h3>Delegating Constructors</h3>
        
            <p>There is another option to initializing private member variables in a class.  The first option is using a default constructor to initialize all the member variables to either null or zero, the second option is initializing them in our main function via the constructor, the third option is using a constructor initializer.
        
        <span class = "nl">However, the new standard c++11 allows us to use a delegate to initialize the private member variables while declaring the constructor itself.  Let's take a look at this.</span></p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    class Person {
    public:
        Person(int x, int y) : var(x), var2(y) { }
        <span class="comments">// Our Constructor delegate</span>
        Person () : Person (5, 10) { }
    private:
        int var;
        int var2;
    }</pre>
        
