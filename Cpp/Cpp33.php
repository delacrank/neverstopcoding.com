        <h1>Class Scope - Cpp Development</h1>
        
        <h2>Understanding Class Scope</h2>
        
            <p>Understanding scope is something which is very important in programming.  In other sections I showed you how to define a global variable verses a local variable.  Also, I have showed you how to access variables and functions outside our source file using the scope operator '::', like when using functions from the standard library such as 'std::cout' or accessing a container like 'std::string'.
        
        <span class = "nl">There are also scopes within a function called a private variables, the variables within our functions (outside our main function) can be used only within the opening and closing brackets '{' and '}'. One of the ways to access these sorts of variables is by using a pointer '*' or a reference operator '&amp;' in order to access the address of these private variables, and then call or modify their values in our main function.</span>
        
        <span class = "nl">Then we have scope within are classes public and private member variables. Public member variables can be used if we create an object and use the dot access operator '.' (classObject.memberVariable). Private variables can be accessed using a function call in order to 'return' the variable into a public scope. The last way to access private or public variables from a class is uing the 'friend' qualifer for our functions.</span>
        
        <span class = "nl">In this section we are going to go over how to understand the differences in scope within our class, in perticular when accessing parameters and variables within our member functions.</span>
        
        <span class = "nl">Let's take a look at an example below.</span></p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments">// define global int var</span>
    int var = 5;
    
    class Person {
    public:
        <span class = "comments">// which var is returned global or private?</span>
        int getVar() { return var; }
    
    private:
        <span class = "comments">// define private member int var</span>
        int var = 10;
    };
    
    int main()
    {
        Person p;
        
        <span class = "comments">// number 5 for global var, number 10 for private var</span>
        std::cout &lt;&lt; p.getVar() &lt;&lt; "\n";
    }</pre>
        
        <p>If you did the exercise you would see the private variable was returned.  If we changed the code to use the scope operator we could assign the global variable to our return function like so.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments">// This should return the number 5, our global value</span>
    int getVar() { return <span class = "red">::</span>var; }</pre>
        
        <p>In these next examples I will be using the 'this' keyword, the 'this' is pointer which refers to the invoking object.</p>
        
     <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments">// Ensure that we are using a member variable</span>
    int getVar() { return <span class = "red">Person::</span>var; }
    
    <span class = "comments">// Or we could do it this way instead</span>
    int getVar() { return <span class = "red">this-></span>var; }
    </pre>
