<h1>Introduction to Search Engine Optimization</h1>
    
    <p>Search Engine Optimization is understanding how search engines analyze the text content on your web page in order to decide its ranking and revelance to the user.
    
    <span class = "nl">The main purpose of a search engine is to find the content relevant to a users needs. One of the ways it will accomplish this is by using a series of algorithms in order to determine whether or not to display certain web pages based on the search input in the query box.</span></p>
    
    <hr />
    
    <h2>Types of Searches</h2>
    
    <p><span class = "nl">There are typically three types of categories that web pages will fall under.</span></p>
    
    <ul class = 'li'>
        <li><b>Navigational Queries:</b> These types of searches are ones where the user already has an idea of what exactly they are looking for. If a user was to type in Amazon or facebook in a search box, this would mean that he or she simply wants the search engine to provide a link to that site. (hence navigational).</li>
        <li><b>Information Queries:</b> These types of searches are used when a user is trying to aquire information about a particular topic. This could be information pertaining to health, sports, electronics, etc.</li>
        <li><b>Transactional Queries:</b> This type of search will yeild the highest traffic value, meaning that users who perform this type of query are likely to spend money. A search of this type is performed when a user is trying to make a purchase, such as find a place to eat, a store to buy something, or an online retailer for a ticket to a show, etc.</li>
    </ul>
    
    <p>Navigational and Transactional queries will yeild the highest returns for websites but tend to reign in only 20% of the average traffic. Informational queries on the other hand will retain as high as 80% percent of the traffic, so while a user may not immediately purchase an item upon visiting a site, they will probably make a purchase in the future using the information obtained at a later time.
    
    <span class = "nl">In the situation where a user will browse a site and then continue searching for more information, this is called an 'Adpative Search'. An example of this would be a user who is searching for a pair of running shoes, maybe they go to several different sites to research information on best shoes and best prices.</span>
    
    <span class = "nl">This user may even spend as much as 20-30 minutes browsing different sites before making a decision going from an informational query into a transactional query.</span></p>
    
    <hr />
    
    <h2>How Search Engines Interpret Queries</h2>
    
    <p>You may wonder how a search engine decides to display certain sites to a user and not others. One of the most important factors is relevance, if the site actually delivers on what it is advertising. 
        
    <span class = "nl">Relevance can be determined by a number of different factors. One factor is average amount of time users spend on the site itself, if the site is misleading, then users will quickly realize this and leave making their time spent on the site low. Another factor can be how many other sites link to this site. Sometimes a site which can be an authority on a subject like fitness or health may have several other sites or communities (social media, forums, news groups) referencing that site.</span>
        
    <span class = "nl">Another factor can be keywords which the user is typing and the amount of times those words may appear in an article or a website. Finally, the search engine tends to prioritize recently uploaded content above sites which may be stagnant. Believe it or not search engines are so advanced that they can also detect sites which have many spelling errors, long load times or programming errors either.</span>
        
    <span class ='nl'>Sites can also change iun ranking based on the number of users visiting the site at a given time, so even if all the necessary criteria is met in order to obtain a high ranking, another site which is obtaining more traffic may be placed above it.</span></p>
    
    <hr />
    
    <h2>Document Analysis</h2>
        
    <p>Search engines use a variety of tools in order to review websites which consist largely of html files and the text within those html tags. A search engine will generally crawl a website often finding other sites via the use of anchor tags or links.
        
    <span class = "nl">They will take the data they obtain from crawling a website and store that in a database, then they will simply display a list of results which most closely resemble what a user may be searching for. However, they will draw from a variety of methods to determine which sites to show and which sites to avoid showing. Below are some of the terms which are used for determining which sites to list based on the query.</span>
    
    <ul class ='li'>
        <li><b>Proximity Searches: </b> Uses the order of the search phrase to find related documents. When using quotes for a phrase like "American football", the search engine will probably find documents related to american football instead of soccer played in America if the quotes were removed.</li>
        <li><b>Fuzzy logic:</b> Refers to a logic which is either true or false, typing in day is sunny should return information pertaining to days which there is little or no cloud coverage, this is often used to protect against mispelled words as well.</li>
        <li><b>Boolean Searchs:</b> Boolean terms such AND, OR and NOT can be used with the query to return information which either must contain both words, either word or not this word. (similar to SQL query).</li>
        <li><b>Term Weighting:</b> Understands how to emphasis certain words more heavily then others, words that commonly appear in english would not be as heavily used when processing a search (like the word 'the').</li>
        <li><b></b></li>
    </ul>
        
