        <h1>Command Line - Bash</h1>

        <h2>Bash Command Line</h2>
        
            <p>So using touch enables us to update the time next to a modified file and also it allows us to create files as well.  What if we wanted to create an entire directory in which to organize our files.
        
        <span class = "nl">To do this we would need to use another command called mkdir (short for make directory). The directory will be created in our current directory unless we specify so by adding the 'path name' to our new folder.</span>
        
        <span class = "nl">Let's look at an example of using this command.</span></p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments"># Create a directory called 'text_files'</span>
    Machine-Name: 'desktop': UserName: <span class = "red">mkdir text_files</span></pre>
        
        <p>To take it one step further we can create a directory within a directory without having to navigate their first.  The way we can do this is by using the '-p' option to designate the parent folder of the directory we want to create.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments"># Create a child directory within an existing directory</span> 
    Machine-Name: 'desktop': UserName: <span class = "red">mkdir -p text_files/child_dir</span></pre>
        
        <p>If we want to move the file we created in our desktop into this directory we created. We would have to use the move command 'mv'.
            
        <span class ="nl">First we type in the name of the file, then we type in the directory, path or destination. An example of this would be 'mv hello.txt text_files/child_dir'.</span></p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class ="comments"># Move our hello.txt file in our desktop to our text_files directory</span>
    Machine-Name: 'desktop' UserName: <span class ="red">mv hello.txt text_files/child_dir </span></pre>
        
    <p>So far we have learned how to create both files and directories, and move these files.  Let's see if we can use the 'mv' command to change a filename.
        
        <span class = "nl">This can be accomplished by typing in the name of the old file and then typing the name you wish that file to be changed into. Just be sure to be in the current directory of the name you want.</span></p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class ="comments"># Change our hello.txt file to another name</span>
    Machine-Name: 'desktop' UserName: <span class ="red">cd text_files/child_dir</span>
    Machine-Name: 'child_dir' UserName: <span class ="red">mv hello.txt goodbye.txt</span></pre>
    
