        <h1>Sstream (String Stream) - Cpp Standard Library</h1>
        
        <h2>Working with sstream</h2>
        
            <p>The last of the three header streams is called the string stream.  We will use this stream to manipulate strings.  Using this library is the alternative to manually changing characters using either an array or a vector of characters.
        
        <span class = "nl">The string stream is best used for manipulating or searching for information in any document which contains strings.  This can be on the web through an html file, on a computer using a text file, even for editing source code.</span>
        
        <span class = "nl">Let's look at a basic example below.</span></p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    #include &lt;sstream>
    #include &lt;iostream>
    #include &lt;string>
    
    int main()
    {
        std::string word("5");
        std::ostringstream output;
        <span class = "comments">// Place the word string into our ostringstream object</span>
        output &lt;&lt; word;
        std::cout &lt;&lt; output.str() &lt;&lt; std::endl;
    }</pre>
        
        <p>In the example below, we will be taking a string and placing it into our ostringstream object. However, if we want to print this string on to our console, then we would require the use of the 'str()' function.
        
        <span class = "nl">Let's look at an example of trying to use our 'istreamstring' object in the next example below.</span></p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    #include &lt;sstream>
    #include &lt;iostream>
    #include &lt;string>
    
    int main()
    {
        std::string word("5");
        std::istringstream input;
        <span class = "comments">// Initialize our istringstream var to the our string var</span>
        input.str(word);
        std::cout &lt;&lt; input.str() &lt;&lt; std::endl;
    }</pre>
        
        <p>In this example we were able to set our istringstream object to the value in our string by using '.str()' funciton.  
            
            <span class = "nl"></span>Let's see if we can initialize that istringstream to an integer and add that number to another.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    #include &lt;sstring>
    #include &lt;iostream>
    #include &lt;string>
    
    int main()
    {
        std::string word("5");
        int number;
        std::istringstream input;
        input.str(word);
        <span class = "comments">// Take that string value, convert it to an int assign it to our number var</span>
        input >> number;
        std::cout &lt;&lt; number + 5 &lt;&lt; std::endl;
    }</pre>
        
        <p>We can also do this by using are basic stringstream object as well.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    #include &lt;sstream>
    #include &lt;iostream>
    #include &lt;string>
    
    int main()
    {
        std::string word("5");
        int number;
        std::stringstream input;
        <span class = "comments">// Insert the string value into our stringstring object</span>
        input &lt;&lt; word;
        <span class = "comments">// Assign the value from our string into our number</span>
        input >> number;
        <span class = "comments">// Print the result plus five to our console</span>
        std::cout &lt;&lt; number + 5 &lt;&lt; std::endl;
    }</pre>    
        
        <p>So those are some the benifts of using the stringstream class. Our next section will focus on using sequential containers.</p>
        
