<!-- Break -->
<h3>Break</h3>
    
    <p>A break puts the loop on pause for a moment in order to do something else.  So, it allows the user a chance to provide input, or to change the value of a variable, or change the flow again.  Note, that once we break the loop without continuing none of the code within the loop will be executed from the moment we broke it.

    <span class="nl"> In the example bellow, we are going to take a break in order to multiply i by two.</span></p>
    
    
<!-- Example -->
<em>Example:</em>
<!-- Code section -->
<pre class = "code">
#include &#60; iostream &#62;
using namespace std;

int main()
{
    int i;
    for (i = 0; i &lt; 10; i++)
    { 
        cout &lt;&lt; i &lt;&lt; " ";
        if (i == 5)
        {
            break;
        }
        cout &lt;&lt; i * 2 &lt;&lt; endl;
    }
    cout &lt;&lt; " All done!" &lt;&lt; endl;
    return 0;
}</pre>
    
<p>Let's take a moment to look at the output on the console.  We see that i is being incremented by 1 on the left side, and then there is a space proceeded by another cout statment which is writen after the if statement.

<span class="nl">Once the loop hits the fifth iteration it will write only the first cout statement and then stop immediately, proceeding to execute other instructions written outside of the loop.</span>
</p>

<hr />

<!-- Continue -->
<h3>Continue</h3>

    <p>    The continue statement is similar to the break statemenet, where the loop will stop its current iteration in order to perform a function, but afterwards it will continue with the rest of the iterations of the loop. </p>

<!-- Example -->
<em>Example:</em>
<!-- Code section -->
<pre class = "code">
#include &#60;iostream&#62;
using namespace std;

int main()
{
    int i;
    for (i = 0; i &lt; 10; i++)
    {
        cout &lt;&lt; i &lt;&lt; " ";
        if (i == 5)
        {
            continue;
        }
        cout &lt;&lt; "i * 2" &lt;&lt; endl;
    }
    cout &lt;&lt; " All done!" &lt;&lt; endl;
    return 0;
}</pre>

    <p> If we look at the output of the console, you will notice how the loop goes from 5th iteration to the 6th iteration using the continue statement, yet it skips the second cout statement on the fifth iteration.  There is no ten written in the console.</p>

