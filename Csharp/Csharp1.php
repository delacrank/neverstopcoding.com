<!-- Implicit vs Explicit Declarations -->
<h3 >Implicit vs Explicit Declarations</h3>
    
    <p>Implicitly typed local variables can also be defined by using the keyword 'var'. For example, if I want to declare an int and assign it to the value of three I can do it by using 'var'.</p>
    
<!-- Code -->
<em>Code:</em>
<pre class="code">
var three = 3;</pre>
    
<p>I can also do this for a string, Implicitly is literally what it means, the variable is defined as the value 'implies'.</p>
    
<!-- Code -->
<em>Code:</em>
<pre class="code">
var name = "Carlos"; </pre>
    
<p> Of course you can still use explicit definitions for your 'int' and 'string' variables.</p>
    
<!-- Code -->
<em>Code:</em>
<pre class="code">
int three = 3; 
string name = "Carlos";</pre>
    
<p>The problem with implicit declarations is that if you have two variables with the same name, one being an object and the other being an integer, the compiler might get confused on knowing which one is which, say for example we try to assign one of those variables to another variable.</p>
    
