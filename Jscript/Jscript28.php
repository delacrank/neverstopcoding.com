<h2>Creating elements</h2>
    
    <p>The document object also contains a function which is necessary for the creation of other elements, 'createElement'. 
    
    <span class = "nl">In order to use this function you must use quotes to enclose the html you wish to create.</span></p>

    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments">// create the element here</span>
    var y  = document.createElement('p');
    
    <span class = "comments">// display the name</span>
    console.log(y.nodeName);</pre>
    
<hr />
    
<h2>Text type</h2>
    
    <p>Text is node type 3, it exists within the element node and is basically just the text within html elements.  The text type has a variety of different functions: appendData, deleteData, insertData, replaceData, splitText and substringData.
    
    <span class = "nl">These functions are important for adding, removing or replacing text from nodes. Let's see how we can combine our create element and our text node, in order to create html in a script and attach it to our DOM tree.</span>
    
    <span class = "nl">The text node can be created using the property 'nodeValue' or using the function 'createTextNode'. You can also attach a text node to an element using the 'appendChild' function, and you can attach nodes to other nodes using the 'after' function.</span>
    
    <span class = "nl">Let's look at an example of how we can create element nodes and attach text nodes to them.</span></p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments">// our html</span>
    &lt;div id = 'test'> &lt;/div>
    
    <span class = "comments">// our script</span>
    var v = document.getElementById('test');
    
    <span class = "comments">// create the element node</span>
    var x = document.createElement('p');
    
    <span class = "comments">// create a text node</span>
    var z = document.createTextNode('good night sir');
    
    <span class = "comments">// use the append function to attach the text node</span>
    x.append(z);
    
    <span class = "comments">// add both of these nodes to a div</span>
    v.appendChild(x);</pre>
    
    <p>If we already had an existing text node and just wanted to manipulate the value of that node, we could do so using the 'nodeValue' property.</p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments">// our html</span>
    &lt;div id = 'test'> something &lt;/div>
    
    <span class = "comments">// our script</span>
    var v = document.getElementById('test');
    
    <span class = "comments">// change the value of the node</span>
    v.firstChild.nodeValue = 'something else';</pre>
    
