<h1>Javascript - Events Into</h1>
    
    <p>In this section we will be talking how to add user interaction to our javacript.  This is largely based on the form of an event handler or a listener.  Meaning that the browser will listen for specific events via keyboard or mouse in order to determine what to do.</p>
    
<hr />

<h2>Event bubbling &amp; Event Capture</h2>
    
    <p> Events which are triggered happen through either event bubbling (Internet explorer) or event capture (netscape). This means that there is a ripple effect which happens based on all the elements in the dom tree.  The difference between the two forms is how the event listener detects the user input, from the parent element down to the child element (event capture) or from the child element up towards the parent (event bubbling).
    
    <span class = "nl">For cross browser compatibility event bubbling is generally used.</span></p>
    
<hr />
    
<h2>Adding events</h2>
    
    <p>Events can be added in one of three ways, via: html, javascript or an eventListner function. Let's look at examples of each.</p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class ="comments">// html</span>
    <span class ="comments">// add the event inside the onclick attribute</span>
    &lt;p onclick = "alert('say hi')"> click &lt;/p>
    
    <span class ="comments">// or we can add the event to a function</span>
    &lt;p onclick = 'sayhi()'> 2nd click &lt;/p>
    
    <span class ="comments">// script</span>
    function sayhi() {
        alert('say hi');
    }</pre>
    
    <p>This is how we could add an event via html, we could give the html element an attribute and then assign that attribute some code via javascript. Although to go and edit any of our functions would be tedious to change all of the html elements, so the second way would be to add a pointer to a function, then write the function in the script.
    
    <span class = "nl">The second way is more idea yet if we wanted to eventually change or remove an event then we would need to go through all of our html and remove or change the name of our attribute. Let's look at another way to add an event using the dom properties.</span></p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class ="comments">// html</span>
    &lt;p id = 'ok1'> click &lt;/p>
    
    <span class ="comments">// script</span>
    var x = document.getElementById('ok1');
    
    <span class ="comments">// assign the event to an anonymous function</span>
    x.onclick = function() {
        alert('say hello');
    }
    
    <span class ="comments">// here we can remove the event handler</span>
    x.onclick = null;</pre>
    
<hr />
    
<h2>Adding and Removing Event Listeners</h2>
    
    <p>Let's look at the third and most effective way to add an event, using the 'addEventListener()' function. The beauty of this function is that it allows us to add multiple events to a single element, where the other ways limited us to a single event per handler.
    
    <span class = "nl">First let's see how we can add a single event, notice that the 'addEventListener()' function takes 3 arguments: type of event, function or pointer to function and false or true for  bubbling capture. Where a false value would use event bubbling and a true value would use event capture.</span></p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class ="comments">// html</span>
    &lt;p id = 'ok1'> click &lt;/p>
    
    <span class ="comments">// script</span>
    var x = document.getElementById('ok1');
    
    <span class ="comments">// add an event listener to the element</span>
    x.addEventListener('click', function () {
        alert("say hello"); }, 
    false);</pre>
    
    <p>The problem with using an anonymous function, is that we can remove the event listener later. Let's create a pointer to a function and then add the pointer. If we decide to remove the function afterwards, the 'removeEventListener()' function has a pointer to refer to.</p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class ="comments">// html</span>
    &lt;p id = 'ok1'> click &lt;/p>
    
    <span class ="comments">// script</span>
    var x = document.getElementById('ok1');
    
    <span class ="comments">// create a pointer to a function here</span>
    var sayhello = function() { 
        alert('say hello');
    };
    
    <span class ="comments">// add the pointer to the listener</span>
    x.addEventListener('click', sayhello, false);
    
    <span class ="comments">// remove the listener here</span>
    x.removeEventListener('click', sayhello, false);</pre>
    
    <p>Let's see an example of how we could add multiple events using the addEventListener function.</p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class ="comments">// html</span>
    &lt;p id = 'ok1'> click &lt;/p>
    
    <span class ="comments">// script</span>
    var x = document.getElementById('ok1');
    
    <span class ="comments">// create a pointer to a function here</span>
    var sayhello = function() { 
        alert('say hello');
    };
    
    var saybye = function() { 
        alert('sayh bye');
    };
    
    <span class ="comments">// add the pointer to the listener</span>
    x.addEventListener('click', sayhello, false);
    
    <span class ="comments">// add another event to the listener</span>
    x.addEventListener('click', saybye, false);</pre>
    
    <p>So far we have just worked with the click or onclick event, however there are many different types of event listeners which listen to mouse, keyboard, and even browser events such as scrolling or resizing.
    
    <span class = "nl">In our next section we will go over the event object and the different types of events.</span></p>
    
