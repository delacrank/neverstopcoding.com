<h2>Understanding the DOM</h2>

    <p>The Document object model consistents of different nodes, each related to each other in a hierarchy. Some of the nodes higher up in this hierarchy are referred to as the parent type, the ones lower in it are referred to as the children of those parents.
    
    <span class = "nl">This might make more sense if we look at an image of the DOM.</span></p>
    
    <img src="../Images/DOM-model.svg" style='width: 100%'/>
    
    <p>Here we see the top of the DOM tree consists of an element called document and then nested in that element we have a child called html. The html element has 2 children which refer to each other as sibilings.
    
    <span class = "nl">One thing to note here is that even the text are considered nodes in the DOM.  Let's see how we can use the first node type in our code to understand the DOM at a deeper level.</span></p>
    
    <hr />
    
<h2>Node Type 1</h2>
    
    <p>There are 12 types of nodes in ascending order: Element, attribute, text, cdata_section, entity_reference, entity, processing_instruction, comment, doucment, document_type, document_fragment and notation node.
    
    <span class ="nl">The first node is the element node, which can be identified by its nodeType property.</span></p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments">// our html</span>
    &lt;p id = 'ok'>hello&lt;/p>
    
    &lt;script>
    <span class = "comments">// select our p node by id</span>
    var x = document.getElementById('ok');
    
    <span class = "comments">// returns true if nodetype is 1</span>
    console.log(x.nodeType == 1);
    
    <span class = "comments">// we can print out the type of element</span>
    console.log(x.nodeName);
    
    <span class = "comments">// determine the length of the first child element</span>
    console.log(x.childNodes[0].length);
    &lt;/script></pre>
    
    <p>This is great if we are working with an element who has no real children, what if we are working with an unordered list which has 3 list item children. Let's see how we can use the node values to determine the names, values and lengths of the 'ul'.
    
    <span class = "nl">Note, that in order to properly detect the amount of childnodes which are stored in an array, you must remove any white space within the list. So, rather then using tabs or newlines just write the unordered list without indenting or using enter.</span></p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments">// our html</span>
    &lt;ul id = 'ok1'>&lt;li>first&lt;/li>&lt;li>second&lt;/li>&lt;li>third&lt;/li>&lt;/ul>
    
    &lt;script>
    <span class = "comments">// select our ul node by id</span>
    var x = document.getElementById('ok1');
    
    <span class = "comments">// print name of the element</span>
    console.log(x.nodeName);
    
    <span class = "comments">// determine the amount of children in the parent node</span>
    console.log(x.childNodes.length);
    
    <span class = "comments">// display value of first child</span>
    console.log(x.childNodes[0]);
    
    <span class = "comments">// display value of second child</span>
    console.log(x.childNodes[1]);

    <span class = "comments">// display name of first child</span>
    console.log(x.childNodes[0].nodeName);
    &lt;/script></pre>
    
    <p>The nodes also contain properties like 'nextSibiling' and 'previousSibiling', let's use the same html example only using these next properties this time.</p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    <span class = "comments">// our html</span>
    &lt;ul id = 'ok2'>&lt;li>first&lt;/li>&lt;li>second&lt;/li>&lt;li>third&lt;/li>&lt;/ul>
    
    &lt;script>
    <span class = "comments">// select the ul list by using the id attribute</span>
    var x = document.getElementById('ok2');
    
    <span class = "comments">// if we select the first list and check for the next</span>
    console.log(x.childNodes[0].nextSibling);
        
    <span class = "comments">// if we select the last item </span>
    console.log(x.childNodes[2].previousSibling);
    &lt;/script></pre>
    
