        <h1>Selectors - CSS Web Development</h1>
        
        <h2>Selectors</h2>
        
            <p>In this section we are going to talk about using selectors or combinations of selections together in order to define which html elements are going to be modified with our css styles. One thing you should note about selectors is that there are 5 or 6 different types of selectors based the html element tree.</p>
        
        <ul id="li">
            <li><b>Ancestors:</b> An ancestor is the selector which wraps other elements.</li>
            <li><b>Descendant:</b> The descendant of an ancestor is the element inside another tag, so the &lt;body> tag is the descendant of the &lt;html> tag.  And the &lt;h1> tag is the descendant of the &lt;body> tag.</li>
            <li><b>Parent:</b> The parent is the next closest ancestor to any descandent.  So an &lt;li> tag can be the descandent of an &lt;html> tag, but only the &lt;ul> (or &lt;ol> ) tag can be it's parent.</li>
            <li><b>Child:</b> The child is just the next closest descandent of the tag, so a list item element is the child of an unordered list element.</li>
            <li><b>Sibiling:</b> A sibiling is one of many child tags inside a parent.  So if a list has multiple elements inside an order/unordered list, then all the children are called sibilings.</li>
        </ul>
        
        <h2>Psuedo Selectors</h2>

            <p>In css we learned in previous sections how to select certain phases of a 'a' tag.  Such as visited, active, hover, and link.  One of the ways we were able to accomplish this is by using a colon to access the psuedo selector, 'a:hover'.<br /><br />
        
        In this section we are going to go into other psuedo selector and psuedo class options.</p>
        
        <hr />
        
