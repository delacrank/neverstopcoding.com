        <h3>:first-letter, :first-line</h3>
        
            <p>The first letter or first line selector works by changing the either the first letter or first line of the element.  Let's look at an example of this.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    .p:first-letter {
        font-size: 60px;
