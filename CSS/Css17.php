    <h3>Attribute Selectors</h3>
    
        <p>An attribute selector is a selector we can use in order to change all elements with a certain type of html attribute.  Let's like the attribute 'title' for an 'img' tag, for every html element with title attribute in their image tag, apply this style.</p>
        
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    img[title] {
        color: blue;
    }</pre>
        
    <p>We can go one step further and make it so that only elements with the value of 'hello' in their title attribute are selected. </p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    img[title="hello"] {
        color: blue;
    }</pre>
        
    <p>We can also modify this so that only images that end with the file extension .png are selected. This is accomplished by using the dollar sign prior to the assignment operator.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    img[src$=".png"] {
        color: blue;
    }</pre>
        
    <p>Or make it so that only images come from a certain folder, by adjusting the beginning of the attribute selector. This is accomplished by using the caret ( ^ ) symbol prior to the assignment operator.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    img[src^="../Images2/"] {
        color: blue;
    }</pre>
        
        <p>Selectors which use the caret ^ symbol will only modify elements who have the attribute value which begins with the quoted words. While those selectors which use the dollar symbol only refer to attribute values whose quoted values are in the end of the attribute.
        
    <span class = "nl">There is also a way to use a universal attribute selector which adds this style to any attribute which contains the value reguardless of it's location. That selector is the asterisk star *. Let's look at an example.</span></p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    &lt;style type = "text/css">
    
    p[title^="goodnight"] {
        color: red;
    }
    
    p[title$="goodnight"] {
        color: blue;
    }
    
    p[title*="moon"] {
        background-color: yellow;
    }   
    &lt;/style>
    
    &lt;p title = "goodnight moon"> hello &lt;/p>
    
    &lt;p title = "moon goodnight"> hello &lt;/p>
    
    &lt;p title = " goodnight "> hello &lt;/p></pre>
        
    <br />    
        
    <em>Example:</em>
    <!-- example --><br />
    
    <p title = "goodnight moon"> hello </p>
    
    <p title = "moon goodnight"> hello </p>
    
    <p title = " goodnight "> hello </p>
        
    <p>Notice how the last element wasn't changed in color, this is because it is neither in front or towards the back of the attribute value's quotes. The rule is that the style has to be touching either the front quote to use the ^, or the back quote to use the $, selector.</p> <hr />  
        
