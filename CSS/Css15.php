<!-- Drop down for mobile -->
<h3>Drop down for mobile</h3>
    
    <p>In order to make the drop down menu work for mobile devices, you should add the 'onmouseover=""' tag to your upper most list item.
    
    <span class = "nl">This is because many mobile devices don't respect the ':hover' feature. You must actually first click on the item first in order for the drop down menu to appear. This is simply a change in the html. </span>
    
    <span class = "nl">Let's see an example of this.</span></p>
    
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    &lt;ul> 
        &lt;li <span class= "red">onmouseover = ""</span>>Social Networking 
            &lt;ul>
                &lt;li> &lt;a href = "http://www.digg.com"> dig &lt;/a> &lt;/li> 
                &lt;li> &lt;a href = "http://www.facebook.com"> facebook &lt;/a> &lt;/li>   
                &lt;li> &lt;a href = "http://www.stumbleupon.com"> stumbleupon &lt;/a> &lt;/li>
            &lt;/ul>
        &lt;/li>
    &lt;/ul></pre>
    
    <p>Thats pretty much it, nothing changes in the CSS, you just change the display from 'none' to 'block' when the hover effect is invoked on the child of the unordered list.</p>
    
