        <h1>Box Sizing, Border Radius - CSS Web Development</h1>
        
        <h2>Box Sizing, Border Radius</h2>
        
            <p>In this section we are going to talk about the total size of elements within a box, this includes the margin, padding and content. We will also go over how to manage content between the three and how to round corners of a box.</p>
        
        <hr />
        
        <h3>Box Sizing</h3>
        
            <p>The box sizing property comes with three types of value: content-box, padding-box, border-box. A box with the content-box sizing property is the default, this means that width and height are based solely on the contents of a the inside of the box, excluding the margin and padding. </p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    div.box {
        box-sizing: content-box;
        width: 100px;
        height: 50px;
        background-color: grey;
        padding: 20px;
        margin: 20px;
    }
    
    &lt;div class = "box"> &lt;/div></pre>
        
    <br />
    <em>Example</em><br />
    <!-- example -->
    <div class="box"></div>
        
    <p>The box above should be exactly 100 pixels wide and 50 pixels in height, then add to that 20 pixels in every direction for padding and another 20 pixels outside for margins. That makes it 140 pixels wide and 90 pixels high, with 40 pixels of width and height to seperate it from other elements.
        
    <span class = "nl">Let's look at another example with box-sizing: border-box. There is also padding-box but only mozilla is supporting that feature.</span></p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    div.box {
        box-sizing: border-box;
        width: 100px;
        height: 50px;
        background-color: grey;
        padding: 20px;
        margin: 20px;
    }
    
    &lt;div class = "box"> &lt;/div></pre>
        
    <br />
    <em>Example</em><br />
    <!-- example -->
    <div class="box1"></div>
        
    <p>Here we see that the box size has been greatly reduced. This is because the 'border-box' will keep the content at whatever the height and width are set at, reguardless of padding or margin.</p>
        
    <hr />
        
    <h3>Border Radius</h3>
        
        <p>Now, let's talk about border radius. Border radius allows us to create rounded corners to our borders. Let's look at an example of this.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    div.box {
        border-radius: 5px;
        width: 100px;
        height: 50px;
        background-color: grey;
    }
    
    &lt;div class = "box"> &lt;/div></pre>
        
    <br />
    <em>Example</em><br /><br />
    <!-- example -->
    <div class="box2"></div>
        
        <p>By increasing the radius we can make the corners even more rounded.  Or we can adjust the radius of just one corner and set the other corners to different radiuses.</p>
        
    <em>Code:</em>
    <!-- code -->
    <pre class="code">
    div.box {
        border-radius: 25px;
        width: 100px;
        height: 50px;
        background-color: grey;
    }
    
    &lt;div class = "box"> &lt;/div></pre>
        
    <br />
    <em>Example</em><br /><br />
    <!-- example -->
    <div class="box3"></div>
        
    <p>And with different corners.</p>    
        
    <em>Code:</em>
    <!-- code -->
