<!-- Validating Input -->    
<h3>Validating Input</h3>
    
    <p>Javascript also gives us the ability to check our input field and determine whether or not that input is the correct type or if it is even filled at all.</p>
    
<em>Code:</em>
<!-- Code -->
<pre class="code">
&lt;label> Input a number between 1 and 10: &lt;/label>
&lt;input id="numb" type="number">    
&lt;button type="button" onclick="myFunction()"> Submit &lt;/button>&lt;br />
    
&lt;label id="demo">&lt;/label>
    
&lt;script>
function myFunction() {
     var x, text;
    <span class="comments">// Get the value of the input field</span>
    x = document.getElementById("numb").value;
        
    <span class="comments">// Check the number for proper value</span>
    if (isNaN(x) || x &lt; 1 || x > 10) {
    text = "Incorrect number";
    } else {
        text = "Input OK";
    }
    document.getElementById("demo").innerHTML = text;
}
&lt;/script></pre>
<br />
    
<!-- Example -->
<em>Example:</em><br /><br />
    
<label> Input a number between 1 and 10: </label>
<input id="numb" type="number">
<button type="button" onclick="myFunction()"> Submit </button><br />
