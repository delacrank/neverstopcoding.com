<h1>Manipulating the DOM - Javascript Web Development</h1>

<h2>Manipulating the DOM</h2>
    
    <p>In this section we will talk about how to change our css styles using javascript, as well as changing our html contents using buttons.</p>
    
<hr />    
    
<!-- Changing CSS styles -->
<h3>Changing CSS styles</h3>
    
    <p>Let's see how we can manipulate our paragraph element to display a different style.</p>
    
<em>Code:</em>
<!-- Code -->
<pre class="code">
&lt;label id = "demo"> ok &lt;/label>
    
&lt;script>
function myFunction() { 
    var x = document.getElementById("demo");
    x.style.fontSize = "25px";
    x.style.color = "red"; 
}
&lt;/script>
    
&lt;button type="button" onclick="myFunction()"> Change style &lt;/button></pre>


<br />    
    
<!-- Example -->    
<em>Example:</em>    
<label id = "demox">ok</label> 

