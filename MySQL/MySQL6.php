    <h3>String Functions</h3>
        
        <p>I will list several of the functions used in MySQL however I will not show or use examples for every single function.
            
    <span class = "nl">Below, is a list of string functions that can be used on strings.</span></p>
        
        
    <ul class = "li">
        <li><b>Concat(t1, t2):</b> Will combine two or more text together.</li>
        <li><b>Concat_WS(s, t1, t2):</b> Will combine a string and two or more text together.</li>
        <li><b>Length(t):</b> Will write the number of characters in text.</li>
        <li><b>Left(t, y):</b> Will write all the characters to the left of the y parameter.</li>
        <li><b>Right():</b> Will write all the of the characters to the right of the x parameter.</li>
        <li><b>Trim():</b> Removes any excess spaces at the beginning or end of the text.</li>
        <li><b>Upper(t):</b> Captialize all of the text.</li>
        <li><b>Lower(t):</b> Lowercase all of the text.</li>
        <li><b>Replace():</b> Replace one string with another one.</li>
        <li><b>Substring(t, x, y):</b> Will substitute a part of the string (t) based on a position (x) and  for a certain length (y).</li>
    </ul>

