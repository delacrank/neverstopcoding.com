<h1>Functions - PhP Programming</h1>
    
    <h2>Functions</h2>
    
    <p>So previously we have used many functions when coding with php.  To name a few we have used the: print(), isset(), print_r(), array(), if(), for(), foreach(), while(), etc.
        
        <span class = "nl">However, we don't need to use functions already created in PhP, we can use our own functions by simply declaring them and then calling them when we want to use them.</span>
        
        <span class = "nl">To declare a function all you have to do is use the function keyword prior to writing  the name of the function, let's look at an example below.</span></p>
    
    <em>Code:</em>    
    <!-- code -->
    <pre class="code">
    &lt;?php
        <span class="comments">// Declare our function</span>
        function writeHello () {
            print "hello";
        }
        <span class="comments">// Call our function</span>
        writeHello(); 
    &#63;></pre>
    
<br />
<em>Example:</em><br /><br />
<!-- Example -->
<?php
function writeHello () {
print "hello";
}
writeHello();
?> 
