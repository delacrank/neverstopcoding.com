<!-- passing functions into variables -->
<h3>Defining functions as variables</h3>
    
    <p>We can also just define a function as a variable which will make it easier to use in our code.
        
        <span class = "nl">However, if we are to use our functions as variables then they need to return a value otherwise they will be null. So, in our next example we will use the 'return' keyword to set the function equal to something.</span></p>
    
    <em>Code:</em>    
    <!-- code -->
    <pre class="code">
    &lt;?php
        function print_hello () {    
            return "hello";
        }
        $hello  = print_hello();
        print "&lt;p> I would just like to say $hello &lt;/p>";
    &#63;></pre>    
   
<br />
<em>Example:</em><br /><br />    
<!-- Example -->    
<?php
function print_hello () {
return "hello";
}
$hello = print_hello();
print "<p> I would just like to say $hello </p>";
?> 
    
