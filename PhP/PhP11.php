<!-- parameters -->
<h3>Parameters and Arguements</h3>

<p>That was a very simple example of me just using declaring a function which would print a single word.  I can make it more complicated by giving the function parameters and then passing arguements into my parameters to perform calculations.</p>
    
    <em>Code:</em>    
    <!-- code -->
    <pre class="code">
    &lt;?php
        <span class="comments">// Declare our function and parameters</span>
        function Add ($a, $b) {
            print $a + $b;
        }
        <span class="comments">// Call our function and pass arguements into it</span>
        Add(1, 2);
    &#63;></pre>
    
<br />    
<em>Example:</em><br /><br />
<!-- Example -->
<?php
function Add ($a, $b) {
print $a + $b;
}
Add(1, 2);
?>   
