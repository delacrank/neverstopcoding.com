<!-- local and global variables -->    
<h3>Local vs Global variables</h3>
    
<p>How about passing variables into my functions, and what about the scope of my variables global vs local variables. Variables defined within the function will be considered local while variables defined outside the function are considered global.
    
<span class = "nl">In PhP we need to specify which variable we are using, because if we try to pass a global variable into a local function without letting the function know by using the global keyword, it will remain undefined and produce a runtime error.</span></p>    
    
    <em>Code:</em>    
    <!-- code -->
    <pre class="code">
    &lt;?php
        <span class="comments">// Declare a variable</span>
        $output = "ok";
        function print_output () {
            <span class="comments">// Initialize the variable within the scope our function</span>
            global $output;
            print $output;
        }
        print_output();
    &#63;></pre>
    
<br />
<em>Example:</em><br /><br />   
<!-- Example -->    
<?php
$output = "ok";
function print_output () {
global $output;
print $output;
}
print_output();
?> 
