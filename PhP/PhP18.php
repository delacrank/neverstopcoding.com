<!-- MySQL Queries -->
<h3>MySQL Queries</h3>
        
        <p>We have also learned how to use several SQL queries in order to create our database, table, insert values into that table and select records from that table.  Below is a list of some of those queries.</p>
        
    <ul class = "li">
    <li><b>CREATE:</b> Can be used to either create a table or a database followed by the name.</li>
     <li><b>DROP:</b> Can be used to delete either a table or database etc.</li>
     <li><b>IF (NOT) EXISTS:</b> Checks to see if the item is/isn't available before performing any actions.</li>
     <li><b>INT:</b> defines the dataset being used for a field as an integer.</li>
      <li><b>VARCHAR:</b> defines the dataset used for the field variable characters (string or single char).</li>
      <li><b>PRIMARY KEY:</b> Sets a field for an entire table as a primary key (a way of identifying the record).</li>
      <li><B>INSERT INTO `table name` VALUES:</B> Will place the values into records if they are of the correct dataset (use quotes for characters or strings).</li>
      <li><B>SELECT:</B> Selects a record or field.</li>
      <li><b>FROM:</b> Used to determine from which table the record or field will be choosen from.</li>
    </ul>
        
        <p>There are many more SQL queries and PhP functions to learn, I am just covering the basic ones used so far in this tutorial.</p>
        
