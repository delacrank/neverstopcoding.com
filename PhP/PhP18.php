        <h2>PhP MySQL Functions</h2>
        
        <p>Alright, so we were able to use our website in order to verify that we have infact created a database, a table and inserted these values into our table.  Below are a list of some the PhP functions we used in order to perform all of these actions.</p>
        
        <ul class = "li">
            <li><b>mysqli_connect:</b> Connect our website to a server using a username, password and database.</li>
            <li><b>mysqli_connect_error():</b> Display an error message relevant to the reason for the failed connection.</li>
            <li><b>mysqli_query:</b> Use this connection string and SQL query to perform these actions to our server, to either send, recieve or remove data from our database.</li>
            <li><b>mysqli_multi_query:</b> Same as mysqli_query only multiple queries.</li>
            <li><b>mysqli_fetch_assoc:</b> Take data from our mysqli_query and store it in this variable.</li>
            <li><b>mysqli_close:</b> Close the connection to our server.</li>
        </ul>
        
