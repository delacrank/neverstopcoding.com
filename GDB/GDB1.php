        <h1>Instructions - Gnu Debugger</h1>
        
        <h2>Using GDB</h2>
        
            <p>So previously we went over how to install and get GDB up and running.  We also learned that in order to debug programs using GDB we need to compile with 'clang++' (if on a mac machine).  
        
        <span class = "nl">In section we will learn how to step into various functions or variables and obtain the values they contain.  We can also learn how to set breakpoints within our program in order to check errors that may be buggy.</span>
        <span class ="nl">Once you type in 'gdb test' (gdb executable filename) gdb should appear in the terminal. It should look like this.</span></p>
                
        <br />
        <img src="../Images/gdb%20term.png">
        
        <p>If we want to run the program using the debugger, we can simply type in 'run'.  Generally, you program would run as it normally would.  However, if you want to see the source code of your program and understand where you can place break points, then you can try the 'list' command.</p>
       
<ul id="li">        
    <li><b>List:</b> Displays the source code of the program.</li>
    <li><b>Run:</b> Runs the program.</li>
</ul>
        
        <p>Once you have become familiar using those commands, let's try adding a breakpoint to our program.  We can set the break point typing in 'b' or 'break' then the number or function where we want the program to break and allow us to inspect the code.
        
        <span class = "nl">Now, if you run the program it will halt at the break point and you can do several things from here on out. You can back trace several lines before the program hits the break point 'bt' or 'backtrace', step into the function 's' or 'step', move to the next line of code 'n' or 'next', or print values 'print x'.</span></p>
        
<ul id="li">        
    <li><b>Break 'b':</b> Sets a break point in a designated area in the program.</li>
    <li><b>Backtrace 'bt':</b> Displays several instructions prior to the break point.</li>
    <li><b>Step 's':</b> Steps into a function.</li>
    <li><b>Next 'n':</b> Moves on to the next line of code.</li>
    <li><b>Print 'p':</b> Prints the value of a variable or function.</li>
</ul>
        
        <p>Let's say that we are steping through the program normally, but then we realize that we want to look at some code which we passed over already.  For this you can type in 'start'.  That will allow you to go to the beginning of the program.  
        
        <span class = "nl">If for example you want to see how a perticular variable changes over the course of your program, a good command to use would be 'watch'. This command will display the value of that variable every time it changes. There is also 'read watch' which displays the value of that value everytime it is read into another variable.</span>
        
        <span class = "nl">Another command is called 'continue' if the program is stopped at a break point and you don't want to start it from the beginning, then typing in continue will allow it to run until the next break point or until it has completed.</span></p>
        
<ul id="li">        
    <li><b>Start:</b> Resets the program to begin again.</li>
    <li><b>Watch 'w':</b> Will watch to see the value of a variable change over time.</li>
    <li><b>Read watch 'rwatch':</b> Watches the value of a variable everytime it is read into another variable.</li>
    <li><b>Contine 'cont':</b> Contines the program until another break point is hit or the program is finished.</li>
</ul>
        
